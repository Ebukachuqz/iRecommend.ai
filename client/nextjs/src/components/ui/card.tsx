import * as React from "react"

import { cn } from "@/lib/utils"

function Card({
  className,
  size = "default",
  ...props
}: React.ComponentProps<"div"> & { size?: "default" | "sm" }) {
  return (
    <div
      data-slot="card"
      data-size={size}
      className={cn(
        "command-card group/card flex flex-col gap-4 overflow-hidden p-6 text-body-md text-text-primary data-[size=sm]:p-5",
        className
      )}
      {...props}
    />
  )
}

